

numero = int(input ("INGRESE NÚMERO: "))

if numero % 2 == 0:
    print ("EL NÚMERO ES MULTIPLO DE 2")

elif numero % 3 == 0:
    print ("EL NÚMERO ES MULTIPLO DE 3")

elif numero % 4 == 0:
    print ("EL NÚMERO ES MULTIPLO DE 4")

elif numero % 5 == 0:
    print ("EL NÚMERO ES MULTIPLO DE 5")

else:
    print ("EL NÚMERO NO ES PAR")
