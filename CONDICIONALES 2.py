

numero = int(input ("INGRESE NÚMERO A VALIDAR: "))

if numero == 1:
    print ("EL DÍA ES DOMINGO")

elif numero == 2:
    print ("EL DÍA ES LUNES")

elif numero == 3:
    print ("EL DÍA ES MARTES")

elif numero == 4:
    print ("EL DÍA ES MIÉRCOLES")

elif numero == 5:
    print ("EL DÍA ES JUEVES")

elif numero == 6:
    print ("EL DÍA ES VIERNES")

elif numero == 7:
    print ("EL DÍA ES SÁBADO")

else:
    print ("EL NÚMERO NO ES VALIDO")
